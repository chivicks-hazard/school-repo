#include <iostream>
using namespace std;

int main() {
    // Integer
    int integerVar = 42;
    cout << "Interger Variable: " << integerVar << endl;
    
    // Floating-point number
    float floatVar = 3.14159;
    cout << "Floating-Point Variable: " << floatVar << endl;
    
    // Character
    char charVar = 'A';
    cout << "Character Variable: " << charVar << endl;
    
    // String
    string stringVar = "Hello, World!";
    cout << "String Variable: " << stringVar << endl;

    // Double Precision
    double doubleVar = 3.14;
    cout << "Double Precision Variable: " << doubleVar << endl;   
    return 0;
}
